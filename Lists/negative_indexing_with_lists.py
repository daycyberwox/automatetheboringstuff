# Negative Indexing

listOne = ["cat", "rat", "dog", "elephant"]
listThree = ["cat", "rat", "dog", "elephant"]
print(listThree[-1])        # Prints the last item in the list
print(listThree[-2])        # Prints the second to the last item in the list


print("The " + listThree[-1] + " is afraid of the " + listOne[1] )